// Aura Herbs Sharing Script
window.openAuraHerbsShare = function(shareTitle, shareDesc, shareUrl) {
    // Show the modal
    const modal = document.getElementById('auraherbs-share-modal');
    if (!modal) {
        console.error("Aura Herbs Share Modal not found in DOM");
        return;
    }
    modal.style.display = 'flex';

    // 2. Format data for specific platforms
    // WhatsApp accepts custom text + the link
    const whatsappText = encodeURIComponent(`${shareTitle}\n${shareDesc}\n${shareUrl}`);
    
    // Other platforms predominantly use the URL and scrape your HTML Meta Tags for the preview
    const encodedUrl = encodeURIComponent(shareUrl);
    const encodedTitle = encodeURIComponent(shareTitle);

    // 3. Update the button hrefs
    document.getElementById('share-wa').href = `https://api.whatsapp.com/send?text=${whatsappText}`;
    document.getElementById('share-fb').href = `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`;
    document.getElementById('share-tw').href = `https://twitter.com/intent/tweet?url=${encodedUrl}&text=${encodedTitle}`;
    document.getElementById('share-li').href = `https://www.linkedin.com/sharing/share-offsite/?url=${encodedUrl}`;

    // 4. BONUS: Web Share API (Native mobile sharing menu for text/images)
    if (navigator.share) {
        const nativeBtn = document.getElementById('share-native');
        if (nativeBtn) {
            nativeBtn.style.display = 'flex';
            nativeBtn.onclick = async () => {
                try {
                    await navigator.share({
                        title: shareTitle,
                        text: shareDesc,
                        url: shareUrl
                    });
                    console.log('Shared successfully');
                } catch (err) {
                    console.error('Error sharing:', err);
                }
            };
        }
    }
};
